
public class pattern1_1 {
	

		public static void main(String[] args) {

			int n = 5;

			int row = 1;
			
			while (row <= n) {

				// work
				int col = 1;
				while (col <= n) {
					System.out.print("*");
					col++;
				}

				// prep
				row++;
				System.out.println();

			}

		}

	}


